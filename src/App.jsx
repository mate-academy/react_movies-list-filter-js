import './App.scss';
import { useState } from 'react';
import { MoviesList } from './components/MoviesList';
import moviesFromServer from './api/movies.json';

export const App = () => {
  const [getvalue, setGetValue] = useState('');

  const visibleMovies = moviesFromServer.filter(elem => {
    const searchElement = getvalue.toLocaleLowerCase().trimStart().trimEnd();

    return (
      elem.title.toLowerCase().includes(searchElement) ||
      elem.description.toLowerCase().includes(searchElement)
    );
  });

  return (
    <div className="page">
      <div className="page-content">
        <div className="box">
          <div className="field">
            {/* eslint-disable-next-line jsx-a11y/label-has-associated-control */}
            <label htmlFor="search-query" className="label">
              Search movie
            </label>

            <div className="control">
              <input
                onChange={event => setGetValue(event.target.value)}
                value={getvalue}
                type="text"
                id="search-query"
                className="input"
                placeholder="Type search word"
              />
            </div>
          </div>
        </div>

        <MoviesList movies={visibleMovies} />
      </div>

      <div className="sidebar">Sidebar goes here</div>
    </div>
  );
};
